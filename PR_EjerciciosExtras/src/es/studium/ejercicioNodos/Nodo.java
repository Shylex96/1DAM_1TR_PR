package es.studium.ejercicioNodos;

public class Nodo
{
	// Declaración de las variables:
	private int pos;
	private int sig;
	private int valor;
	
	
	
	// Getter & Setter:
	public int getPos()
	{
		return pos;
	}
	public void setPos(int pos)
	{
		this.pos = pos;
	}
	public int getSig()
	{
		return sig;
	}
	public void setSig(int sig)
	{
		this.sig = sig;
	}
	public int getValor()
	{
		return valor;
	}
	public void setValor(int valor)
	{
		this.valor = valor;
	}
	
	
	
	
	
	
}
